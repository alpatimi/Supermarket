    public void updateTabelKeranjang() {
    keranjangModel.setRowCount(0); // clear dulu
    for (CartItem item : keranjang) {
    keranjangModel.addRow(new Object[]{
            item.getProductId(),
            item.getNama(),
            item.getKategori(),
            item.getHarga(),
            item.getStock(),
            item.getTotalHarga()
        });
    }
  }




        


   
