    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
      int selectedRow = tabelKeranjang.getSelectedRow();
    if (selectedRow != -1) {
        int productIdToDelete = (int) tabelKeranjang.getValueAt(selectedRow, 0); // ambil ID produk dari kolom ke-0

        // cari dan hapus item dari list keranjang
        for (int i = 0; i < keranjang.size(); i++) {
            if (keranjang.get(i).getProduct().getId() == productIdToDelete) {
                keranjang.remove(i);
                break;
            }
        }

        updateTabelKeranjang(); // refresh isi tabel
        JOptionPane.showMessageDialog(this, "Produk berhasil dihapus dari keranjang.");
    } else {
        JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus.", "Peringatan", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_deleteBtnActionPerformed

    
    
       
